// INCREMENT AND DECREMENT OPERATORS
#include <stdio.h>

void main()

{

 int a = 10;
 float b = 10.5;

 printf("a++ = %d \n", a++); //a++ First prints and then increments
 printf("++a = %d \n", ++a); //++a First increments and then prints
 printf("b-- = %f \n", b--); //b-- First prints and then decrements
 printf("--b = %f \n", --b); //--b First decrements and then prints

 getch();

}
