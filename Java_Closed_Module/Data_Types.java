//PROGRAM TO PRINT THE DEFAULT VALUES OF THE DATA TYPES

package Java_Closed_Module;

public class Data_Types {
	
	byte b;     // 0
	short s;    // 0
	int i;      // 0
	long l;     // 0
	char c;     // null character
	String st;  // null character
	float f;    // 0.0
	double d;   // 0.0
	boolean bl; // false
	
		public static void main(String[] args) {
			
			Data_Types obj = new Data_Types();
			System.out.println(obj.b);
			System.out.println(obj.s);
			System.out.println(obj.i);
			System.out.println(obj.l);
			System.out.println(obj.c);
			System.out.println(obj.st);
			System.out.println(obj.f);
			System.out.println(obj.d);
			System.out.println(obj.bl);

	}

}
